// agri-drone-accounts/server/config/database.js
import mongoose from 'mongoose';
import dotenv from 'dotenv';

dotenv.config();

const connectDB = async () => {
  try {
    const conn = await mongoose.connect(process.env.MONGODB_URI || 'mongodb://localhost:27017/agri-drone-accounts');

    console.log(`üçÉ MongoDB Connected: ${conn.connection.host}`);
    
    // Handle connection events
    mongoose.connection.on('error', (err) => {
      console.error('‚ùå MongoDB connection error:', err);
    });

    mongoose.connection.on('disconnected', () => {
      console.log('üîå MongoDB disconnected');
    });

    // Graceful shutdown
    process.on('SIGINT', async () => {
      await mongoose.connection.close();
      console.log('üîå MongoDB connection closed through app termination');
      process.exit(0);
    });

  } catch (error) {
    console.error('‚ùå MongoDB connection failed:', error.message);
    console.log('‚ö†Ô∏è  Server will continue in mock mode');
    // Don't exit the process, let server run with mock data
    return false;
  }
};

export default connectDB;
